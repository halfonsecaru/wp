/**
 * Enum para tamaños definidos de iconos
 * Mapea nombres descriptivos a dimensiones en píxeles.
 * 
 * @example
 * ```typescript
 * // En un componente Angular
 * size: AlfIconSizeEnum.Medium // Icono de 32x32 píxeles
 * ```
 */
export enum AlfIconSizeEnum {
    XSmall = 16,
    Small = 24,
    Medium = 32,
    Large = 48,
    XLarge = 64,
    XXLarge = 96
}
