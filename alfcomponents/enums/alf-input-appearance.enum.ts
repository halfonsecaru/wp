/**
 * Enum para los estilos de apariencia del input (inspirado en Angular Material)
 */
export enum AlfInputAppearanceEnum {
  /** Estilo con contorno y label que se integra en el borde (Notch) */
  Outline = 'outline',
  /** Estilo con fondo relleno y línea inferior de énfasis */
  Fill = 'fill',
  /** Estilo minimalista solo con línea inferior */
  Standard = 'standard'
}
