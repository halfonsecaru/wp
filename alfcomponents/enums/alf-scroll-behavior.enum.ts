/**
 * Enum para la propiedad CSS scroll-behavior
 * Define el comportamiento del desplazamiento (instantáneo o suave).
 * 
 * @example
 * ```typescript
 * // En un componente Angular
 * scroll: AlfScrollBehaviorEnum.Smooth // Desplazamiento fluido y animado
 * ```
 */
export enum AlfScrollBehaviorEnum {
  Auto = 'auto',
  Smooth = 'smooth',
}
