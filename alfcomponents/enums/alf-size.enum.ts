/**
 * Enum para tamaños estándar de componentes (Core)
 * Controla dimensiones generales como altura, padding y escala.
 * 
 * @example
 * ```typescript
 * // En un componente Angular
 * size: AlfSizeEnum.Sm // Genera escala pequeña (Small)
 * ```
 */
export enum AlfSizeEnum {
  XS = 'xs',
  SM = 'sm',
  MD = 'md',
  LG = 'lg',
  XL = 'xl',
  XXL = '2xl'
}
