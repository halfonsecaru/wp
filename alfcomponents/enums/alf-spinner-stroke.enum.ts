/**
 * Enum para los grosores de trazo del spinner (stroke-width)
 */
export enum AlfSpinnerStrokeWidthEnum {
  /** Trazo muy fino (ej. para botones minúsculos) */
  Hairline = '1',
  /** Trazo fino */
  Thin = '2',
  /** Trazo estándar */
  Base = '3',
  /** Trazo grueso */
  Thick = '4',
  /** Trazo muy pesado */
  Heavy = '6',
}
