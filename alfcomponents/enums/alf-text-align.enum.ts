/**
 * Enum para la alineación de texto (Text Align)
 * Controla la distribución horizontal del texto dentro de su contenedor.
 * 
 * @example
 * ```typescript
 * // En un componente Angular
 * align: AlfTextAlignEnum.Center // Centra el texto horizontalmente
 * ```
 */
export enum AlfTextAlignEnum {
  Left = 'left',
  Right = 'right',
  Center = 'center',
  Justify = 'justify',
  Start = 'start',
  End = 'end',
}
