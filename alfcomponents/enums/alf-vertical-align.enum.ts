/**
 * Enum para la propiedad CSS vertical-align
 * Controla la alineación vertical de elementos inline, inline-block o celdas de tabla.
 * 
 * @example
 * ```typescript
 * // En un componente Angular
 * align: AlfVerticalAlignEnum.Middle // Alineación centrada verticalmente
 * ```
 */
export enum AlfVerticalAlignEnum {
  Baseline = 'baseline',
  Top = 'top',
  Middle = 'middle',
  Bottom = 'bottom',
  TextTop = 'text-top',
  TextBottom = 'text-bottom',
  Sub = 'sub',
  Super = 'super',
}
